#include<bits/stdc++.h>
using namespace std;
int main(){
    int day = 4;
    switch (day){
        case 1 :
            cout << "mon";
            break;
        case 2 :
            cout << "tue";
            break;
        case 3 :
            cout << "wed";
            break;
        case 4 :
            cout << "thu";
            break;
        default :
            cout << "Invalid";
    }

    return 0;
}